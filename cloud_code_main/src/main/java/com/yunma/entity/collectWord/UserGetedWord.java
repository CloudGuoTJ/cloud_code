package com.yunma.entity.collectWord;

public class UserGetedWord {
	
	private Integer id;
	
	private String collectedWord;
	
	private String openId;
	
	private Integer orderId;
	
	private Integer productId;
	
	private Integer vendorId;
	
	private String shortCode;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCollectedWord() {
		return collectedWord;
	}

	public void setCollectedWord(String collectedWord) {
		this.collectedWord = collectedWord;
	}

	public String getOpenId() {
		return openId;
	}

	public void setOpenId(String openId) {
		this.openId = openId;
	}

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public Integer getProductId() {
		return productId;
	}

	public void setProductId(Integer productId) {
		this.productId = productId;
	}

	public Integer getVendorId() {
		return vendorId;
	}

	public void setVendorId(Integer vendorId) {
		this.vendorId = vendorId;
	}

	public String getShortCode() {
		return shortCode;
	}

	public void setShortCode(String shortCode) {
		this.shortCode = shortCode;
	}
	
	
}
